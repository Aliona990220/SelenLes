public class SimpleOperations {
    public static void main(String[] args) {
        int a;
        int b;
        a = 15;
        b = 10;
        System.out.println(a == b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a != b);
        System.out.println(a + 5 == b + 10);
        System.out.println(!(a + 5 == b + 10));
        System.out.println(a < b);
        System.out.println(a > b);
    }
}
